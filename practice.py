num = 25

print(num)

print(f"{num:x}") #  convert num to hex value & change to a string

print(hex(num))     # converts num to its hex value with prefixing

print(f"{num:b}")  #  convert num to binary value & change to a string

print(bin(num))     # converts num to its binary value with prefixing

print(int("11001",2)) # converts string of binary number to an integer

print(int("20012",3)) # converted a base 3 number to decimal value 

print(int("E14", 16)) # converts a base 16 number to deciaml value

